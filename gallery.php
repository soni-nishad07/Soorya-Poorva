                </h3>
                <p><b>India Organic Certificate: </b>This certificate, issued by The Agriculture and Processed Food
                    Products Export Development Authority (APEDA), is internationally recognized and allows you to
                    export your organic produce. The certification is based on the National Programme for Organic
                    Production (NPOP).</p>
                <h3>Application Process:</h3>
                <ul>
                    <li>Check the list of accredited certification bodies under NPOP on the APEDA website.</li>
                    <li>
                        Select an agency and contact them for detailed procedures or download the application form from
                        their website.</li>
                    <li>
                        Submit the filled form along with supporting documents to the agency.</li>
                    <li>
                        The cost for certification is approximately Rs. 40,000 to 50,000 for 56 acres of land.</li>
                </ul>
            </div>

            <div class="sec-wood">
                <h3>2. For Domestic Market:
                </h3>
                <p><b> PGS Organic Certificate:</b> This free certificate, provided through regional councils under the
                    Ministry of Agriculture and Farmers Welfare, allows you to supply organic produce within India. </p>

                <div class="application">
                    <h3>Application Process:</h3>
                    <ul>
                        <li>Visit the PGS India website.</li>
                        <li>If you are an individual farmer, join an existing local group or form a new group with at
                            least 5 members.</li>
                        <li>Register on the PGS India website and submit the required details.</li>
                        <li>After approval, you will receive a PGS India Green certificate for the initial three years,
                            followed by a full PGS India Organic certificate.</li>
                    </ul>
                </div>

